class Buddy {
  final String id;
  final String name;
  final String role;
  final String department;
  final String imageUrl;
  final bool isOnline;
  final List<String> specialties;
  final String email;
  final String phone;

  Buddy({
    required this.id,
    required this.name,
    required this.role,
    required this.department,
    required this.imageUrl,
    required this.isOnline,
    required this.specialties,
    required this.email,
    required this.phone,
  });
}